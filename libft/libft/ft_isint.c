#include "libft.h"

int	ft_isint(char *s)
{
	int l;

	l = ft_strlen(s);
	if (l >= 7 && (!ft_strequ(s, "-2147483648") || ft_strequ(s, "2147483647")))
		return (0);
	while (*s)
		if (!ft_isdigit(*s++))
			return (0);
	return (1);
}
